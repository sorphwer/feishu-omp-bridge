import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { join } from 'node:path';
import { paths } from '../config/paths';
import { getGuestToolAllowlist, type AppConfig } from '../config/schema';
import { log } from '../core/logger';

/**
 * Guest sandbox: when a non-trusted sender triggers a run, the bridge spawns
 * OMP with three independent layers so a stranger can only ever call the
 * whitelisted host tools:
 *
 *  1. `--tools <allowlist>` — strips built-in tools (bash/eval/write/edit/...)
 *     down to the allowlist. Host tools are registered separately and survive.
 *  2. `--config <overlay>` — disables every discovery source so config-sourced
 *     MCP servers (e.g. an arbitrary-code `node_repl`) never load. Cleanup so
 *     the model isn't even shown them.
 *  3. `--extension <hook>` — a fail-closed `tool_call` hook that hard-blocks
 *     ANY tool whose name is not in the allowlist. This is the actual
 *     enforcement boundary; (1) and (2) just shrink the surface. The hook
 *     catches anything (1)/(2) miss (bundled MCP, generate_image, future leaks).
 *
 * Trusted senders never go through any of this — they get the full tool set.
 */

/**
 * OMP discovery sources that can contribute MCP servers / tools from external
 * tool configs (`~/.claude.json`, `~/.codex`, etc.). Disabling all of them
 * keeps a guest run from inheriting the operator's personal MCP toolbelt.
 */
const DISCOVERY_SOURCES = [
  'native',
  'claude',
  'codex',
  'gemini',
  'github',
  'opencode',
  'cursor',
  'agents-md',
];

/**
 * `--tools` value forcing ZERO built-ins. OMP treats an unknown tool name as
 * "allow nothing matching" rather than an error, and an empty `--tools` is
 * dropped (reverting to full defaults), so we need a non-empty sentinel.
 */
const NO_BUILTINS_SENTINEL = '__bridge_no_builtins__';

const overlayPath = join(paths.guestDir, 'overlay.yml');
const hookPath = join(paths.guestDir, 'allowlist-hook.mjs');

export function guestOverlayYaml(): string {
  return [
    '# AUTO-GENERATED by feishu-omp-bridge guest sandbox. Do not edit.',
    'tools:',
    '  discoveryMode: off',
    '# No shared memory for guests: blocks Memory Guidance injection AND the',
    '# retain/recall/reflect tools, so a stranger can neither read nor poison',
    '# the operator\'s memory bank.',
    'memory:',
    '  backend: "off"',
    'disabledProviders:',
    ...DISCOVERY_SOURCES.map((s) => `  - ${s}`),
    '',
  ].join('\n');
}

export function guestHookSource(allowlist: string[]): string {
  return [
    '// AUTO-GENERATED by feishu-omp-bridge guest sandbox. Do not edit.',
    '// Fail-closed tool allowlist: blocks every model tool call whose name',
    '// is not explicitly permitted for sandboxed (non-trusted) senders.',
    `const ALLOWED = new Set(${JSON.stringify(allowlist)});`,
    'export default function hook(pi) {',
    '  pi.on("tool_call", async (event) => {',
    '    if (!ALLOWED.has(event.toolName)) {',
    '      return { block: true, reason: "feishu-omp-bridge guest sandbox: tool \'" + event.toolName + "\' is not permitted for this sender." };',
    '    }',
    '  });',
    '}',
    '',
  ].join('\n');
}

let cachedSignature: string | undefined;

/** Idempotently (re)write the overlay + hook files. Cached by allowlist signature. */
async function ensureArtifacts(allowlist: string[]): Promise<void> {
  const signature = JSON.stringify(allowlist);
  if (cachedSignature !== signature) {
    await mkdir(paths.guestDir, { recursive: true });
    await writeIfChanged(overlayPath, guestOverlayYaml());
    await writeIfChanged(hookPath, guestHookSource(allowlist));
    cachedSignature = signature;
    log.info('guest', 'artifacts-ready', { allowlist });
  }
}

async function writeIfChanged(path: string, content: string): Promise<void> {
  const existing = await readFile(path, 'utf8').catch(() => undefined);
  if (existing === content) return;
  await writeFile(path, content, 'utf8');
}

export interface GuestRunArgs {
  tools: string;
  configOverlayPaths: string[];
  extensionPaths: string[];
}

/**
 * Resolve the per-run OMP args that sandbox a guest sender. Generates the
 * overlay + hook artifacts on demand. The returned `tools` strips built-ins to
 * the allowlist (or zero, via sentinel, when the allowlist has no built-ins).
 *
 * Note: the guest system prompt is NOT injected here — it's prepended to the
 * user prompt in channel.ts. `--append-system-prompt` was found to hang the
 * codex (gpt-5.5) request intermittently.
 */
export async function buildGuestRunArgs(cfg: AppConfig): Promise<GuestRunArgs> {
  const allowlist = getGuestToolAllowlist(cfg);
  await ensureArtifacts(allowlist);
  return {
    tools: allowlist.length > 0 ? allowlist.join(',') : NO_BUILTINS_SENTINEL,
    configOverlayPaths: [overlayPath],
    extensionPaths: [hookPath],
  };
}
